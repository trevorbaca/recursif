def _make_short_instrument_names():
    short_instrument_names = dict()
    for staff_number in range(1, 64 + 1):
        markup = abjad.ShortInstrumentName(rf"\markup \hcenter-in #8 {staff_number}")
        short_instrument_names[str(staff_number)] = markup


def make_empty_score():
    site = "recursif.make_empty_score()"
    tag = abjad.Tag(site)
    global_context = baca.score.make_global_context(make_time_signatures_context=True)
    staves = []
    for staff_index in range(64):
        staff_number = staff_index + 1
        voice = abjad.Voice(name=f"Percussion.Voice.{staff_number}", tag=tag)
        staff = abjad.Staff([voice], name=f"Staff.{staff_number}", tag=tag)
        staves.append(staff)
    staff_group = abjad.StaffGroup(staves, name="StaffGroup", tag=tag)
    music_context = abjad.Context(
        [staff_group],
        lilypond_type="MusicContext",
        name="MusicContext",
        tag=tag,
    )
    score = abjad.Score([global_context, music_context], name="Score", tag=tag)
    baca.score.assert_lilypond_identifiers(score)
    baca.score.assert_unique_context_names(score)
    return score


def part_manifest():
    return (*[baca.Part("Percussion", _) for _ in range(1, 64 + 1)],)


def rhythm(time_signatures, voice_number, page_number):
    assert page_number in range(1, 16 + 1)
    start_measure_number = 16 * (page_number - 1) + 1
    stop = start_measure_number + 16
    measure_numbers = range(start_measure_number, stop)
    tuplet_ratios = []
    for measure_number in measure_numbers:
        total = 255 + voice_number - measure_number
        count = voice_number - 1
        count = int(abjad.math.binomial_coefficient(total, count) % 8)
        if 0 < count:
            tuplet_ratios.append(count * (1,))
        else:
            tuplet_ratios.append((-1,))
    rhythm_maker = rmakers.stack(
        rmakers.tuplet(tuplet_ratios),
        rmakers.beam_runs(),
        rmakers.extract_trivial(),
        tag=baca.helpers.function_name(inspect.currentframe()),
    )
    music = rhythm_maker(time_signatures, voice_number, page_number)
    return music


